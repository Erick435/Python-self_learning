# Example of a list
fruits = ['apple', 'orange', 'banana', 'lemon']

print(fruits[0]) # 0 is the first in the list
print(fruits[-1]) #-1 is the last index

fruits[1] = "mandarin" # changes the 2 item to new string

print(fruits)

# Extending
fruits.extend(["Orange", "Dragonfruit", "Lime"])
print(fruits)